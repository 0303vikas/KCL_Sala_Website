



$(document).ready(function(){
	// When user clicks on submit comment to add comment under post
    $("#get_comments").click(function(){

        $("#get_comments").css("display","none");
    	$("#hide_comments").css("display","unset");
    	$("#comment_div").css("display","unset");

});

    $("#hide_comments").click(function(){
        $("#get_comments").css("display","unset");
    	$("#hide_comments").css("display","none");
    	
    	$("#comment_div").css("display","none");
});
    	
    	
    });


	
	









